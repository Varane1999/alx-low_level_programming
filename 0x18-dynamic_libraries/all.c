#include <stdio.h>
/**
 * mod - mod two integers
 * @a: first unit
 * @b: second unit
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two integers
 * @a: first unit
 * @b: second unit
 * Return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * Return: subtract
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - division of two integers
 * @a: first unit
 * @b: second unit
 * Return: division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * add - includes two integers
 * @a: first unit
 * @b: second unit
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
